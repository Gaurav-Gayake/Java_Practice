package java_Basics;

public class Variable6 {

	public static void main(String args[]) {
		
		int x=100;// defining local variable
		
		System.out.println(x); // Printing x value
		
		int y; // declaring y variable
		y=x;   // intializing y variable by calling x
		
		System.out.println(y);  // 100
		
	}
}
